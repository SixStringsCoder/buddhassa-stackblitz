				<p>
						Serving at Dhammagiri in the late eighties and early nineties has imprinted itself as the most rewarding experience in my memory. It was then, after the huge crowd of meditators—especially from the West—who had flocked
						in for the winter period, had already disappeared, that only about a dozen of us, mostly Pāli-students, used to stay on for longer periods.
				</p>
				<p>
						In those days a lot of Dhamma-service was still being done by westerners. Aside from regular ongoing duties like early morning gong, hall duty, food service and gardening, there were always sudden and emergent odd jobs.
						The regular Indian paid staff took care of management, registration, cleaning and kitchen work. ...<br />
