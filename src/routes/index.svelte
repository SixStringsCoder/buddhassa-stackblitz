				<h3 style="text-align: center;">Foreword</h3>
				<p>
						It is my great good fortune that I was born and grew up in an extremely devout Indian family settled in Burma. The devotional books of the Gita Press, Gorakhpur, had a great influence on the whole family. My father was a
						devotee of Shiva while my mother was a devotee of Sri Krishna. In the first decade of my life, I used to daily chant either the <em>Vishnu Sahastranama</em> or the <em>Gopal Sahastranama </em>or the
						<em>Shivamahimna Stotra</em> or the <em>Shivartandav Stotra</em> or some chapters of the <em>Gita</em>. I used to enjoy it. Besides strengthening my devotion, another important benefit was that I learned the proper
						pronunciation of Sanskrit and also gained some knowledge of it.
				</p>
				<p>
						In the second decade of my life, my good fortune increased when I came in contact with the Arya Samaj. The teachings jolted my mind. I began to understand the difference between true devotion and bind devotion, true
						faith and blind faith. Instead of blindly accepting everything that I heard or read, I developed the wisdom of examining it and testing whether it was logical and rational. I was indeed fortunate.
